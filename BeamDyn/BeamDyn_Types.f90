    Re_Xferred   = Re_Xferred   + SIZE(InData%bc)
  ENDIF
  IF ( .NOT. OnlySize ) ReKiBuf ( Re_Xferred:Re_Xferred+(1)-1 ) =  (InData%m00 )
  Re_Xferred   = Re_Xferred   + 1
